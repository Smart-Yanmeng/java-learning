import java.awt.*;

public class Rectangle extends BaseShape {

    int width;

    int height;

    public Rectangle(int x, int y, int width, int height, Color color) {
        super(x, y, color);
        this.width = width;
        this.height = height;
    }

    @Override
    public void draw(Graphics graphics) {
        super.draw(graphics);
        graphics.drawRect(x, y, width, height);
    }

    @Override
    public int getWidth() {
        return width;
    }

    @Override
    public int getHeight() {
        return height;
    }
}
